# Числа Фібоначчі

# Кількість чисел у послідовності
n = 10
# Список чисел Фібоначчі (спочатку – дві одиниці)
fibs = [1, 1]

# Повторюємо n - 2 рази, оскільки два числа вже у списку
for i in range(n - 2):
    # Додаємо суму двох останніх чисел
    fibs.append(fibs[i] + fibs[i + 1])

# Виведення списку на екран
print(fibs)
