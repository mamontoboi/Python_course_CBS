# Можна отримувати значення елемента за його індексом (номером).
# Індексація починається з нуля.

# Створення списку чисел
my_list = [5, 7, 9, 1, 1, 2]

# Виведення першого та другого значень
print(my_list[0])
print(my_list[1])

# Введення індексу
index = int(input('Введіть номер елемента: '))

# Отримання елемента
element = my_list[index]

# Виведення значення на екран
print(element)