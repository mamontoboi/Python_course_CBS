# При отриманні зрізів можна вказувати крок.

# Створення списку чисел
my_list = [5, 7, 9, 10, 1, 2]

# Вибір кожного другого значення списку, не включаючи останній елемент
sub_list = my_list[0:-1:2]

# Виведення отриманого списку
print(sub_list)

# Виведення елементів списку від другого (третього) до передостаннього з кроком два
print(my_list[2:-2:2])

# Виведення всіх елементів списку, крім першого, у зворотному порядку
print(my_list[-1:0:-1])
