# Як і інших розглянутих типів даних, типу list,
# існує відповідна функція-конструктор, яка створює список із значення іншого типу.

# Створення порожнього списку
empty_list = list()
print(empty_list)

# Створення списку із послідовності range (актуально для Python 3.x; у Python 2.x range
# і так повертає список, а послідовність створює функція xrange)
numbers = list(range(1000))
print(numbers)

# Створення списку символів з рядка
chars = list("a string")
print(chars)
