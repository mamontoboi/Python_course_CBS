# створення множини
my_set = {1, 3}
print(my_set)
print(id(my_set))

# Такий запис не спрацює
# my_set[0]

# Додавання елемента
my_set.add(2)
print(my_set)
# Вывод: {1, 2, 3}
print(id(my_set))

# додавання кількох елементів
my_set.update([2, 3, 4])
print(my_set)
# Виведення: {1, 2, 3, 4}

# додавання списку та множини одночасно
my_set.update([4, 5], {1, 6, 8})
print(my_set)
# Виведення: {1, 2, 3, 4, 5, 6, 8}
