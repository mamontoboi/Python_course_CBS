# Факторіалом числа n (позначається n!) називається добуток
# всіх натуральних чисел від 1 до n включно.


# приклад рекурсивної функції
def factorial(n):
    if n == 0:
        # умова виходу
        return 1
    else:
        # рекурсивний виклик
        return n * factorial(n - 1)


# обчислення факторіалу числа 5
print(factorial(5))
# обчислення факторіалу числа 25
print(factorial(25))
