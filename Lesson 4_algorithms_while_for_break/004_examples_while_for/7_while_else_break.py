x = 3

while x:  # цикл буде повторено три рази, якщо користувач не завершить його раніше
    x -= 1
    response = input('Введіть stop, щоб зупинити цикл (інакше що завгодно): ')

    if response == 'stop':
        break
else:
    # ця гілка буде виконана тільки якщо цикл не був перерваний
    print('Цикл завершився сам')

print('Кінець програми')
