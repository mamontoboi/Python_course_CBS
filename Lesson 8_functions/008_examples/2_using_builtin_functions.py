# Функція з минулого прикладу
def print_numbers(limit):
    for i in range(limit):
        print(i)


# Читаємо ввідення користувача за допомогою стандартної функції вводу, побудованої з
# його числа допомоги стандартної функції int та записуємо в змінну n
n = int(input('Введіть n: '))

# Викликаємо функцію print_numbers з фактичним параметром n
print_numbers(n)
